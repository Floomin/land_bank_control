def fix_keyboard_layout(text: str) -> str:
    """
    Конвертирует текст, набранный по ошибке в кириллической раскладке (RU/UKR),
    в правильную английскую раскладку (QWERTY).
    """
    if not text:
        return text

    # Словарь сопоставления клавиш (Кириллица -> Латиница)
    layout_map = {
        "й": "q",
        "ц": "w",
        "у": "e",
        "к": "r",
        "е": "t",
        "н": "y",
        "г": "u",
        "ш": "i",
        "щ": "o",
        "з": "p",
        "х": "[",
        "ъ": "]",
        "ї": "]",
        "ф": "a",
        "ы": "s",
        "і": "s",
        "в": "d",
        "а": "f",
        "п": "g",
        "р": "h",
        "о": "j",
        "л": "k",
        "д": "l",
        "ж": ";",
        "э": "'",
        "є": "'",
        "я": "z",
        "ч": "x",
        "с": "c",
        "м": "v",
        "и": "b",
        "т": "n",
        "ь": "m",
        "б": ",",
        "ю": ".",
        ".": "/",
        "ґ": "\\",
        "Й": "Q",
        "Ц": "W",
        "У": "E",
        "К": "R",
        "Е": "T",
        "Н": "Y",
        "Г": "U",
        "Ш": "I",
        "Щ": "O",
        "З": "P",
        "Х": "{",
        "Ъ": "}",
        "Ї": "}",
        "Ф": "A",
        "Ы": "S",
        "І": "S",
        "В": "D",
        "А": "F",
        "П": "G",
        "Р": "H",
        "О": "J",
        "Л": "K",
        "Д": "L",
        "Ж": ":",
        "Э": '"',
        "Є": '"',
        "Я": "Z",
        "Ч": "X",
        "С": "C",
        "М": "V",
        "И": "B",
        "Т": "N",
        "Ь": "M",
        "Б": "<",
        "Ю": ">",
        ",": "?",
        "Ґ": "|",
    }

    # Создаем таблицу перевода
    trans_table = str.maketrans(layout_map)

    # Возвращаем исправленный текст
    return text.translate(trans_table)
